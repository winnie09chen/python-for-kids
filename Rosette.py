# Rosette.py
import turtle
t = turtle.Pen()
t.circle(100) # This make our first circle (pointing north)
t.left(90)    # Then the turtle turns left 90 degrees
t.circle(100) # This make our first circle (pointing west)
t.left(90)    # Then the turtle turns left 90 degrees
t.circle(100) # This make our first circle (pointing south)
t.left(90)    # Then the turtle turns left 90 degrees
t.circle(100) # This make our first circle (pointing east)
